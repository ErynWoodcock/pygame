def שלום_עולם():
    """פונקציה המדפיסה שלום עולם"""
    הודעה = "שלום עולם!"
    print(הודעה)

def חיבור(מספר1, מספר2):
    """פונקציה המחברת שני מספרים"""
    תוצאה = מספר1 + מספר2
    return תוצאה

def תפריט_ראשי():
    """תפריט ראשי"""
    print("ברוכים הבאים!")
    print("בחרו פעולה:")
    print("1. הדפס שלום")
    print("2. חיבור מספרים")
    print("3. יציאה")
    
    בחירה = input("הכניסו בחירתכם: ")
    
    if בחירה == "1":
        שלום_עולם()
    elif בחירה == "2":
        א = int(input("הכניסו מספר ראשון: "))
        ב = int(input("הכניסו מספר שני: "))
        סכום = חיבור(א, ב)
        print(f"הסכום: {סכום}")
    elif בחירה == "3":
        print("להתראות!")
    else:
        print("בחירה לא תקינה")

if __name__ == "__main__":
    תפריט_ראשי()
